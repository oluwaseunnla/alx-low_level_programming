/**
 * op_add - Adds two numbers
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two numbers
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides the number
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modules two numbers
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
